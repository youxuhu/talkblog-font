const __filename = fileURLToPath(import.meta.url)
if (process.argv[1] === __filename) {
  const token = process.argv[2] || TOKEN
  if (!token) {
    console.log('请提供 token: node scripts/seed-blogs.js <your_token>')
    console.log('或者编辑脚本中的 TOKEN 变量')
    process.exit(1)
  }
  seedBlog(token)
}
